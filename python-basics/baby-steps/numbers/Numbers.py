# decimal format:

x = 10
print(F'{"Value is:"} {x}')

# binary format:

x = 0b1010
print(F'{"Value is:"} {x}')

# convert from decimal to binary:

x = bin(42)
print(F'{"Meaning of life:"} {x}')

# want to get all the groupies, show them the hexadecimal format:

x = hex(42)
print(F'{"Meaning of life:"} {x}')
